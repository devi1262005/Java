public class Ope{
public static void main(String args[]){
int a,b;
a=5;
b=4;
System.out.println("Greater:"+(a>b));//Relational
System.out.println("Increment:"+(a++));//Inc
System.out.println("Decrement:"+(--b));//Dec
System.out.println("Shift:"+(a<<b));//Left shift
System.out.println("Shift:"+(a>>b));//Right shift
}
}
